# FILTER BLOG WITH DROPDOWN COURSE NAME -------------------------------------------------------------

class BlogFilterForm(forms.Form):
    blog_name = forms.ModelChoiceField(queryset=Blog.objects.all(), empty_label="All Blog", required=False)
    

# FILTER SERVICES WITH DROPDOWN COURSE NAME -------------------------------------------------------------
class ServicesFilterForm(forms.Form):
    services_name = forms.ModelChoiceField(queryset=Services.objects.all(), empty_label="All Services", required=False)
    


# CONTACT FORM -----------------------------------------------------------------------------------------------------------------------        
class ContactForm(forms.Form):   
    name = forms.CharField(max_length = 50)
    phone = forms.CharField(max_length = 50)
    email = forms.EmailField(max_length = 150)
    subject = forms.CharField(max_length = 50)
    message = forms.CharField(widget = forms.Textarea, max_length = 2000)
    
    
    
    
